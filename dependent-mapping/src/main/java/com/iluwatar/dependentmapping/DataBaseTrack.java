package com.iluwatar.dependentmapping;

/**
 * The type Db track.
 */
class DataBaseTrack {
  private final String name;

  /**
   * Instantiates a new Db track.
   *
   * @param name the name
   */
  DataBaseTrack(String name) {
    this.name = name;
  }

  /**
   * Gets name.
   *
   * @return the name
   */
  String getName() {
    return name;
  }
}